public class Goal {
	public String name;
	public String category;
	public float amount;
	public int priority;
	
	public Goal(String name, String category, float amount, int priority)
	{
		this.name = name;
		this.category = category;
		this.amount = amount;
		this.priority = priority;
	}
}
